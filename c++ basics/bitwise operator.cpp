#include <bits/stdc++.h>
using namespace std;
int main() {
    int a = 3, b = 4;
    int res;
    res = a & b;
    cout << res << "\n";
    res = a | b;
    cout << res << "\n";
    res = a ^ b;
    cout << res << "\n";
    res = a << 1;
    cout << res << "\n";
    res = a >> 1;
    cout << res << "\n";
    return 0;
}
                                                                                                                                                                                                     